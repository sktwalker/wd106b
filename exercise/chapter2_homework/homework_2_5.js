function degree(a)
{
    if(a >= 90)
    {
        console.log("'A'")
    }
    else if(a >= 80)
    {
        console.log("'B'")
    }
    else if(a >= 70)
    {
        console.log("'C'")
    }
    else 
    {
        console.log("'D'")
    }
}
